public class typecasting 
{
    public static void main(String[] args) 
    {
        int a=126;
        byte b=(byte) a;

        float fr=5.6f;
        int in=(int) fr;

        System.out.println("typecasting1="+b);
        System.out.println("typecasting1="+in);
        
    }
    

    
}
