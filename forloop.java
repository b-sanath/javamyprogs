public class forloop 
{
    public static void main(String[] args) 
    {
        int n=5;

        for(int i=1;i<=n;i++)
        {
            System.out.println("Day "+i);

            for(int j=1;j<=9;j++)
            {
                System.out.println("9");
            }


        }
        
    }
    
}
